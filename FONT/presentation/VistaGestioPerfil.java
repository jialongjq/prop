    private void nameBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nameBtnActionPerformed
        String nomUsuari = nameField.getText();
        if (isNullOrEmpty(nomUsuari)) {
            errorMsg("ERROR: El nom d'usuari no pot ser buit");
            return;
        }

        try {
            int retVal = ctrlPresentacio.canviarNom(nomUsuari);

            if (retVal == 0) {
                infoMsg("Canvi de nom de manera correcte");
            } else if (retVal == -1) {
                errorMsg("ERROR: No existeix l'usuari actiu");
            } else if (retVal == -2) {
                errorMsg("ERROR: El nou nom coincideix amb el nom actual");
            } else if (retVal == -3) {
                errorMsg("ERROR: El nom indicat ja esta utilitzat");
            }
        } catch (Exception ex) {
            errorMsg("ERROR: desconegut");
        }
    }//GEN-LAST:event_nameBtnActionPerformed

    private void passBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_passBtnActionPerformed
        String password = new String(passField.getPassword());
        String confPassword = new String(confPassField.getPassword());

        if (isNullOrEmpty(password)) {
            errorMsg("ERROR: La contrasenya no pot ser buida");
            return;
        }

        try {
            int retVal = ctrlPresentacio.canviarContrasenya(password, confPassword);
            if (retVal == 0) {
                infoMsg("Canvi de contrasenya de manera correcta");
            } else if (retVal == -1) {
                errorMsg("ERROR: No existeix l'usuari actiu");
            } else if (retVal == -2) {
                errorMsg("ERROR: La nova contrasenya no coincideix amb la seva confirmaci√≥");
            } else if (retVal == -3) {
                errorMsg("ERROR: La nova contrasenya coincideix amb la contrasenya actual");
            }

        } catch (Exception ex) {
            errorMsg("ERROR: Desconegut");
        }
    }//GEN-LAST:event_passBtnActionPerformed

