    private void listTipusItemValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listTipusItemValueChanged
        List<String> l = listTipusItem.getSelectedValuesList();
        
        boolean selected = l.size() > 0;
        
        btCrearTipusItem.setEnabled(!selected);
        btEliminarTipusItem.setEnabled(selected);
        btModificarTipusItem.setEnabled(selected);
        txtNomTipusItem.setEnabled(!selected);
        
        txtNomTipusItem.setText(selected ? l.get(0) : "");
    }//GEN-LAST:event_listTipusItemValueChanged

