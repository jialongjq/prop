    private void btEliminarTipusItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarTipusItemActionPerformed
        if (txtNomTipusItem.getText().length() == 0) labelInfo.setText("Seleccioneu o introduiu el tipus d'item que voleu eliminiar");
        int retVal = ctrlPresentacio.eliminarTipusItem(txtNomTipusItem.getText());
        if (retVal == -1) labelInfo.setText("El tipus d'item introduit no existeix");
        else if (retVal == -2) labelInfo.setText("Existeix item en el tipus d'item introduit");
        else labelInfo.setText("El tipus d'item introduit s'ha eliminat correctament");
        tipusitem.removeElement(txtNomTipusItem.getText());
        listTipusItem.setModel(tipusitem);
        txtNomTipusItem.setText("");
    }//GEN-LAST:event_btEliminarTipusItemActionPerformed

    private void btModificarTipusItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModificarTipusItemActionPerformed
        // TODO add your handling code here:
        if (txtNomTipusItem.getText().length() == 0) labelInfo.setText("Seleccioneu o introduiu el tipus d'item que voleu eliminiar");
        ctrlPresentacio.vistaGestioTipusAtribut(txtNomTipusItem.getText());
    }//GEN-LAST:event_btModificarTipusItemActionPerformed

    private void btCrearTipusItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCrearTipusItemActionPerformed
        if (txtNomTipusItem.getText().isBlank() || txtNomTipusItem.getText().isEmpty()) {
            labelInfo.setText("Introduiu el tipus d'Ã­tem que voleu crear (no buit)");
            return;
        }
        int retVal = ctrlPresentacio.crearTipusItem(txtNomTipusItem.getText());
        if (retVal == 0) {
            labelInfo.setText("El tipus d'item s'ha creat correctament");
            tipusitem.addElement(txtNomTipusItem.getText());        
        }
        else labelInfo.setText("El tipus d'item introduit ja existeix");
        txtNomTipusItem.setText("");
    }//GEN-LAST:event_btCrearTipusItemActionPerformed

